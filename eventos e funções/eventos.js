/*

Eventos -> Ações que o usuário faz na minha pagína.

EX: Redimensionar a página, mover o mouse, clicar, digitar, etc. 

Eventos do mouse:

onclick - quando o usuário clica no elemento da tela;
ondbclick - quando o usuário clica 2x no elemento;
onmouseup - quando o botão do mouse é liberado, ou seja, deixa de ser pressionado;
onmouseout - quando o usuário tira o mouse da região ocupada pelo elemento.

Eventos do teclado:

onkeydown - é acionado quando uma tecla é pressionada;
onkeypress - é acionado quando eu seguro uma tecla, a "segurando";
onkeyup - é acionado quando uma tecla é liberada.

Eventos da janela:

onresize - acionada quando a página é redimensionada;
onscroll - acionado quando o scroll do mouse é usado.

Eventos de formulario:

onfocus - acionado quando clicamos sob um elemento e damos foco a ele;
onblur - acionado quando um elemento perde o foco;
onchange - acionado quando um elemento é modificado.

*/